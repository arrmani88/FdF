#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int n;

	if (nb < 0)
		ft_putchar('-');
	n = (nb < 0 ? -nb : nb);
	if (n <= 9)
		ft_putchar(n + '0');
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
