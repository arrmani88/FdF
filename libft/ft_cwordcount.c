#include "libft.h"

int	ft_cwordcount(const char *s, char c)
{
	int i;

	i = (s[0] != c ? 1 : 0);
	while (*s)
	{
		if (*s == c && *(s + 1) != c && *(s + 1) != '\0')
			i++;
		s++;
	}
	return (i);
}
