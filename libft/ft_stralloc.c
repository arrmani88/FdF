#include "libft.h"

char	*ft_stralloc(char *s, int len)
{
	if (len > 0 && !(s = (char *)ft_memalloc(len + 1)))
		return (NULL);
	s[len] = 0;
	s[len - 1] = 0;
	return (s);
}
