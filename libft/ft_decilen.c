#include "libft.h"

int	ft_decilen(long long int nb)
{
	int						i;
	unsigned long long int	n;

	i = (nb < 0 ? 2 : 1);
	n = (nb < 0 ? -nb : nb);
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	return (i);
}
